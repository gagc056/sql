--USING NULL

--//SELECT TEACHERS WHO HAVE NULL FOR THEIR DEPARTMENT
SELECT name FROM teacher
  WHERE dept IS NULL;

--//NOTE THE INNER JOIN MISSERS THE TEACHERS WITH NO DEPARTMENT AND DEPARTMENTS WITH NO TEACHER
SELECT teacher.name, dept.name FROM teacher INNER JOIN dept ON (teacher.dept=dept.id);

--//USE A DIFFERENT JOIN SO THAT ALL TEACHERS ARE LISTED
SELECT teacher.name, dept.name FROM teacher LEFT JOIN dept ON (teacher.dept=dept.id);

--//USE A DIFFERENT JOIN SO THAT ALL DEPARTMENTS ARE LISTED
SELECT teacher.name, dept.name FROM teacher RIGHT JOIN dept ON (teacher.dept=dept.id);

--//USE COALESCE TO PRINT THE MOBILE NUMBER USING THE NUMBER '07986 444 2266' IF THERE IS NO NUMBER GIVEN. SHOW TEACHER NAME AND MOBILE NUMBER OR '07986 444 2266'
SELECT name, COALESCE(mobile,'07986 444 2266') FROM teacher;

--//USE THE COALESCE FUNCTION AND A LEFT JOIN TO PRINT THE TEACHER NAME AND DEPARTMENT NAME. USE THE STRING 'None' WHERE THERE IS NO DEPARTMENT
SELECT teacher.name, COALESCE(dept.name,'None') FROM teacher LEFT JOIN dept ON (teacher.dept=dept.id);

--//USE COUNT TO SHOW NUMBER OF TEACHERS AND NUMBER OF MOBILE PHONES
SELECT COUNT(name), COUNT(mobile) FROM teacher;

--//USE COUNT AND GROUP BY dept.name TO SHOW EACH DEPARTMENT AND NUMBER OF STAFF. USE A RIGHT JOIN TO ENSURE THAT THE ENGINEERING DEPARTMENT IS LISTED
SELECT dept.name, COUNT(teacher.name) FROM teacher RIGHT JOIN dept ON (teacher.dept=dept.id) GROUP BY dept.name;

--//USE CASE TO SHOW NAME OF EACH TEACHER FOLLOWED BY 'Sci' IF TEACHER IS IN DEPT 1 OR 2 AND 'Art' OTHERWISE
SELECT teacher.name, CASE WHEN dept.id = 1 OR dept.id = 2 THEN 'Sci' ELSE 'Art' END  FROM teacher LEFT JOIN dept ON (teacher.dept = dept.id);

--//USE CASE TO SHOW NAME OF EACH TEACHER FOLLOWED BY 'Sci' IF THE TEACHER IS IN DEPT 1 OR 2, SHOW 'Art' IF TEACHER'S DEPT IS 3 AND 'None' OTHERWISE
SELECT teacher.name, CASE WHEN dept.id = 1 OR dept.id = 2 THEN 'Sci' WHEN dept.id = 3 THEN 'Art' ELSE 'None' END FROM teacher LEFT JOIN dept ON(teacher.dept = dept.id);
