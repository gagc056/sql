-- SUM and COUNT

--//SELECT THE TOTAL POPULATION OF WORLD
SELECT SUM(population) FROM world;

--//SELECT ALL CONTINENTS
SELECT DISTINCT continent FROM world;

--//SELECT TOTAL GDP OF AFRICA
SELECT SUM(gdp) FROM world
  WHERE continent = 'Africa';

--//SELECT COUNTRIES WITH AN AREA OF AT LEAST 1000000
SELECT count(name) FROM world
  WHERE area >=  1000000;

--//SELECT THE TOTAL POPULATION OF('Estonia', 'Latvia', 'Lithuania')
SELECT SUM(population) FROM world
  WHERE name IN ('Estonia', 'Latvia', 'Lithuania');

--//SELECT FOR EACH CONTINENT THE CONTINENT AND NUMBER OF COUNTRIES
SELECT continent, COUNT(name) FROM world GROUP BY continent;

--//SELECT FOR EACH CONTINET  THE CONTINENT AND NUMBER OF COUNTRIES WITH POPULATION OF AT LEAST 10 MILLION
SELECT continent, COUNT(name) FROM world
  WHERE population >= 10000000 GROUP BY continent;

--//SELECT CONTINENTS THAT HAVE A TOTAL POPULATION OF AT LEAST 100 MILLION
SELECT continent FROM world GROUP BY continent HAVING SUM(population) >= 100000000;