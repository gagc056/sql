--// SELECT NAMES

--//SELECT COUNTRY THAT START WITH Y
SELECT name FROM world
  WHERE name LIKE 'Y%';

--//SELECT COUNTRIES THAT END WITH y
SELECT name FROM world
  WHERE name LIKE '%y';

--//SELECT COUNTRIES THAT CONTAIN THE LETTER x
SELECT name FROM world
  WHERE name LIKE '%x%';

--//SELECT COUNTRIES THAT END WITH land
SELECT name FROM world
  WHERE name LIKE '%land';

--//SELECT COUNTRIES THAT START WITH C AND END WITH ia
SELECT name FROM world
  WHERE name LIKE 'C%ia';

--//SELECT THE COUNTRY THAT HAS oo IN THE NAME
SELECT name FROM world
  WHERE name LIKE '%oo%';

--//SELECT COUNTRIES THAT HAVE THREE OR MORE a IN THE NAME
SELECT name FROM world
  WHERE name LIKE '%a%a%a%';

--//SELECT countries THAT HAVE "t" AS THE SECOND CHARACTER
SELECT name FROM world
 WHERE name LIKE '_t%'
 ORDER BY name;

--//SELECT COUNTRIES THAT HAVE TWO "o" CHARACTERS SEPARATED BY TWO OTHERS
SELECT name FROM world
 WHERE name LIKE '%o__o%'

--//SELECT COUNTRIES THAT HAVE EXACTLY FOR CHARACTERS
SELECT name FROM world
 WHERE name LIKE '____';

--//SELECT COUNTRY WHERE NAME IS THE CAPITAL CITY
SELECT name FROM world
  WHERE name LIKE capital;

--//SELECT country WHERE capital IS THE COUNTRY PLUS "City"
SELECT name FROM world
  WHERE capital LIKE concat(name,' City');

--//SELECT THE CAPITAL AND NAME WHERE THE CAPITAL INCLUDES THE NAME OF THE COUNTRY
SELECT capital, name FROM world
  WHERE capital LIKE concat('%',name,'%');

--//SELECT CAPTIAL AND NAME WHERE CAPITAL IS AN EXTENTION OF NAME OF COUNTRY
SELECT capital, name FROM world
  WHERE capital LIKE concat(name,'_%');

--//SELECT NAME AND EXTENSION WHERE THE CAPITAL IS AN EXTENSION OF NAME OF THE COUNTRY
SELECT name , REPLACE(capital,name,'') as extension FROM world
  WHERE capital LIKE concat(name,'_%');